// Language switching functionality
let currentLanguage = 'de';

function changeLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem('selectedLanguage', lang);
  updatePageContent();
  updateLanguageUI(lang);

  // Close language menu
  document.getElementById('language-menu').classList.remove('show');
  document.getElementById('mobile-language-menu').classList.remove('show');
}

function updatePageContent() {
  const elements = document.querySelectorAll('[data-translate]');

  // Detect which service page we're on
  const path = window.location.pathname;
  let serviceName = '';

  if (path.includes('kinderzahnheilkunde')) serviceName = 'kinderzahnheilkunde';
  else if (path.includes('angstpatienten')) serviceName = 'angstpatienten';
  else if (path.includes('prophylaxe')) serviceName = 'prophylaxe';
  else if (path.includes('zahnerhaltung')) serviceName = 'zahnerhaltung';
  else if (path.includes('aesthetische')) serviceName = 'aesthetische';
  else if (path.includes('zahnersatz')) serviceName = 'zahnersatz';
  else if (path.includes('oralchirurgie')) serviceName = 'oralchirurgie';
  else if (path.includes('implantologie')) serviceName = 'implantologie';
  else if (path.includes('labor')) serviceName = 'labor';

  elements.forEach(element => {
    let key = element.getAttribute('data-translate');

    // For service-specific content (not nav, footer, contact, language, faq.title)
    // prepend the service name to the key path
    const commonPrefixes = ['nav.', 'footer.', 'contact.', 'language.'];
    const isCommonKey = commonPrefixes.some(prefix => key.startsWith(prefix)) || key === 'faq.title';

    // Check if key already has the service name prefix
    const alreadyHasPrefix = serviceName && key.startsWith(serviceName + '.');

    if (serviceName && !isCommonKey && !alreadyHasPrefix) {
      // This is a service-specific key without prefix, prepend service name
      key = `${serviceName}.${key}`;
    }

    const keys = key.split('.');
    // Use window.translations if available (for pages with merged translations like labor)
    // otherwise fall back to serviceTranslations
    let translation = (window.translations && window.translations[currentLanguage]) || serviceTranslations[currentLanguage];

    // Navigate through nested keys
    for (const k of keys) {
      if (translation && translation[k]) {
        translation = translation[k];
      } else {
        translation = null;
        break;
      }
    }

    if (translation) {
      // Update text content or value based on element type
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translation;
      } else {
        element.textContent = translation;
      }
    }
  });

  // Update page title and meta description
  updatePageMeta();
}

function updatePageMeta() {
  // Detect which service page we're on based on body class or URL
  const path = window.location.pathname;
  let serviceName = '';

  if (path.includes('kinderzahnheilkunde')) serviceName = 'kinderzahnheilkunde';
  else if (path.includes('angstpatienten')) serviceName = 'angstpatienten';
  else if (path.includes('labor')) serviceName = 'labor';
  else if (path.includes('prophylaxe')) serviceName = 'prophylaxe';
  else if (path.includes('zahnerhaltung')) serviceName = 'zahnerhaltung';
  else if (path.includes('aesthetische')) serviceName = 'aesthetische';
  else if (path.includes('zahnersatz')) serviceName = 'zahnersatz';
  else if (path.includes('oralchirurgie')) serviceName = 'oralchirurgie';
  else if (path.includes('implantologie')) serviceName = 'implantologie';

  if (serviceName) {
    // Use window.translations if available (for pages with merged translations like labor)
    // otherwise fall back to serviceTranslations
    const translations = (window.translations && window.translations[currentLanguage]) || serviceTranslations[currentLanguage];

    if (translations && translations[serviceName]) {
      const service = translations[serviceName];

      // For labor page, use meta tags if available, otherwise use hero
      if (service.meta) {
        document.title = service.meta.title;
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc) {
          metaDesc.setAttribute('content', service.meta.description);
        }
      } else if (service.hero) {
        document.title = `${service.hero.title} - Zahnarztpraxen Seitschenko-Dinh`;
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc) {
          metaDesc.setAttribute('content', service.hero.subtitle);
        }
      }
    }
  }
}

function updateLanguageUI(lang) {
  const flagMap = {
    'de': 'de',
    'en': 'us',
    'vi': 'vn',
    'ru': 'ru',
    'ar': 'sa'
  };

  const langNames = {
    'de': 'DE',
    'en': 'EN',
    'vi': 'VI',
    'ru': 'RU',
    'ar': 'AR'
  };

  // Update desktop language selector
  const currentFlag = document.getElementById('current-flag');
  const currentLang = document.getElementById('current-lang');
  if (currentFlag && currentLang) {
    currentFlag.src = `https://flagcdn.com/24x18/${flagMap[lang]}.png`;
    currentLang.textContent = langNames[lang];
  }

  // Update mobile language selector
  const mobileCurrentFlag = document.getElementById('mobile-current-flag');
  const mobileCurrentLang = document.getElementById('mobile-current-lang');
  if (mobileCurrentFlag && mobileCurrentLang) {
    mobileCurrentFlag.src = `https://flagcdn.com/24x18/${flagMap[lang]}.png`;
    mobileCurrentLang.textContent = langNames[lang];
  }

  // Update HTML lang attribute
  document.documentElement.lang = lang;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  // Get saved language or default to German
  const savedLanguage = localStorage.getItem('selectedLanguage') || 'de';
  currentLanguage = savedLanguage;
  updateLanguageUI(currentLanguage);
  updatePageContent();

  // Language selector toggle - desktop
  const languageBtn = document.getElementById('language-btn');
  const languageMenu = document.getElementById('language-menu');

  if (languageBtn && languageMenu) {
    languageBtn.addEventListener('click', function(e) {
      e.stopPropagation();
      languageMenu.classList.toggle('show');
      document.getElementById('mobile-language-menu').classList.remove('show');
    });
  }

  // Language selector toggle - mobile
  const mobileLanguageBtn = document.getElementById('mobile-language-btn');
  const mobileLanguageMenu = document.getElementById('mobile-language-menu');

  if (mobileLanguageBtn && mobileLanguageMenu) {
    mobileLanguageBtn.addEventListener('click', function(e) {
      e.stopPropagation();
      mobileLanguageMenu.classList.toggle('show');
      languageMenu.classList.remove('show');
    });
  }

  // Close language menu when clicking outside
  document.addEventListener('click', function() {
    if (languageMenu) languageMenu.classList.remove('show');
    if (mobileLanguageMenu) mobileLanguageMenu.classList.remove('show');
  });

  // Mobile menu toggle
  const mobileMenuBtn = document.getElementById('mobile-menu-btn');
  const mobileMenu = document.getElementById('mobile-menu');

  if (mobileMenuBtn && mobileMenu) {
    mobileMenuBtn.addEventListener('click', function() {
      mobileMenu.classList.toggle('hidden');
    });
  }
});
