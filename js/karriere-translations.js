// Merge with serviceTranslations and create window.translations
if (typeof serviceTranslations !== 'undefined') {
  // Create window.translations by merging serviceTranslations with karriereTranslations
  window.translations = {};
  Object.keys(karriereTranslations).forEach(lang => {
    window.translations[lang] = {
      ...(serviceTranslations[lang] || {}),
      ...karriereTranslations[lang]
    };
  });
} else if (typeof window.translations !== 'undefined') {
  // Merge with existing window.translations
  Object.keys(karriereTranslations).forEach(lang => {
    if (window.translations[lang]) {
      window.translations[lang] = {
        ...window.translations[lang],
        ...karriereTranslations[lang]
      };
    }
  });
} else {
  // If neither exists, just use karriereTranslations
  window.translations = karriereTranslations;
}

// Also export for use in other files
if (typeof module !== 'undefined' && module.exports) {
  module.exports = karriereTranslations;
}
